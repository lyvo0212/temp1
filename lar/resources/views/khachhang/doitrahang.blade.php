    <div class="article-body">
      <p><span class="wysiwyg-underline"><strong>I. Kiểm tra điều kiện đổi trả hàng:</strong></span></p>
<p>Vui lòng chắc chắn rằng sản phẩm quý khách yêu cầu đổi (trả) thỏa mãn điều kiện đổi trả hàng của shop
<ul>
	<li>Đổi - Trả do sai hoặc thiếu sản phẩm</li>
	<li>Trả - Hoàn tiền theo mong muốn của khách hàng trong vòng 30 ngày kể từ khi nhận hàng</li>
	<ul style="margin-left: 10px">
		<li>Sản phẩm còn nguyên tem, mác hay niêm phong của nhà sản xuất</li>
		<li>Sản phẩm còn đầy đủ phụ kiện, quà tặng khuyến mại</li>
		<li>Nếu là sản phẩm điện tử thì chưa bị kích hoạt, chưa có sao ghi dữ liệu vào thiết bị</li>
	</ul>
	<li>Trả - Hoàn tiền sản phẩm lỗi do quá trình vận chuyển</li>
	<li>Trả - Hoàn tiền sản phẩm lỗi sản xuất</li>
</ul>

<p>Một số điều kiện bắt buộc:</p>
<ul>
<li>Sản phẩm không thuộc danh mục hạn chế đổi trả</li>
<li>Không quá 30 ngày kể từ ngày nhận hàng,</li>
<li>Không có dấu hiệu đã qua sử dụng, còn nguyên bao bì đóng gói, đầy đủ phụ kiện, tem hoặc phiếu bảo hành cùng quà tặng kèm theo (nếu có).</li>
</ul>
<p><span class="wysiwyg-underline"><strong>II. Các bước thực hiện đổi - trả:&nbsp;</strong></span></p>
<p><strong>Bước 1: Đăng ký&nbsp;</strong></p>
<p>Liên hệ với Shop qua hotline 19002222 hoặc truy cập&nbsp;trang "<a href="/hc/vi/requests/new" target="_blank">Gửi yêu cầu</a>" và&nbsp;cung cấp thông tin chi tiết về&nbsp;sản phẩm lỗi gồm:</p>
<ul>
<li>Mã số đơn hàng</li>
<li>Tên sản phẩm</li>
<li>Lỗi sản phẩm (mô tả chi tiết và kèm ảnh chụp nếu có)</li>
<li>Nhu cầu cần hỗ trợ: đổi/trả/bảo hành</li>
</ul>
<p><strong>Bước 2:&nbsp;Nhận tin nhắn xác nhận đã đăng ký đổi/trả thành công</strong></p>
<p>Sau khi yêu cầu đổi/trả&nbsp;được chấp nhận, Shop sẽ gửi đến quý khách một tin nhắn có kèm mã miễn phí gửi hàng về Shop thông qua đối tác giao hàng Bưu Điện.</p>
<p><strong>Bước&nbsp;3:&nbsp;Đóng gói và&nbsp;mang sản phẩm đến Bưu Điện</strong></p>
<p>Đóng gói&nbsp;và mang sản phẩm đến bưu điện gần nhà, quý khách có thể tham khảo mạng lưới bưu điện trên toàn quốc tại:&nbsp;<a href="http://www.vnpost.vn/POLocation.aspx" target="_blank">http://www.vnpost.vn/POLocation.aspx</a></p>
<p><strong>Bước 4: Gửi sản phẩm về shop</strong></p>
<p>Điền thông tin mã số đơn hàng lên thùng hàng và gửi hàng về shop theo địa chỉ:&nbsp;Shop Eternal Beauty, 576 Quang Trung P. 11, Q. Gò Vấp, TP.HCM</p>
<p>Trong trường hợp không nhớ thông tin ở <em>bước&nbsp;số 4</em>, quý khách có thể xuất trình tin nhắn shop đã gửi (<em>ở bước 2</em>) cho nhân viên bưu điện và làm theo hướng dẫn.</p>
<p>__</p>
<p><span class="wysiwyg-underline">Lưu ý:</span> Quý khách vui lòng chỉ gởi sản phẩm qua đường bưu điện và chịu trách nhiệm về&nbsp;trạng thái nguyên vẹn của sản phẩm khi gửi về Shop. Quý khách có thể ghi nhớ mã bưu gửi và tra cứu thông tin kiện hàng gửi trả&nbsp;tại:&nbsp;<a href="http://www.vnpost.vn/vi-vn/dinh-vi/buu-pham?key=CQ724476204VN" target="_blank">http://tracuudinhvi.hcmpost.vn/dv/</a></p>
<p>Sau khi&nbsp;nhận được sản phẩm quý khách gởi về, Shop sẽ phản hồi và cập nhật thông tin trên từng giai đoạn xử lý&nbsp;đến quý khách qua email.</p>
